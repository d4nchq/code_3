def slice_string():
    #запитуємо слово у користувача
    word = input("Введіть слово: ")
    
    #визначаємо середину слова
    middle_index = len(word) // 2
    
    #отримуємо зріз: кожний 2-й символ від 5-го до середини.
    sliced = word[4:middle_index:2]  #4 - це 5-й символ, бо індексація починається з 0
    
    #виводимо результат
    print("Результат зрізу:", sliced)

#викликаємо функцію
slice_string()